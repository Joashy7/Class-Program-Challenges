#include <iostream>
using namespace std;

int main() { //Organized this way to seperate the lines
	cout << "A char is stored in " << sizeof(char) << " bytes." << endl;
	cout << "An unsigned short int is stored in " << sizeof(unsigned short int) << " bytes." << endl;
	cout << "An int is stored in " << sizeof(int) << " bytes." << endl;
	cout << "A long is stored in " << sizeof(long) << " bytes." << endl;
	cout << "A float is stored in " << sizeof(float) << " bytes." << endl;
	cout << "A double is stored in " << sizeof(double) << " bytes." << endl;
}